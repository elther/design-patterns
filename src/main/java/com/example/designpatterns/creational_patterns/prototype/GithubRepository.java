package com.example.designpatterns.creational_patterns.prototype;

public class GithubRepository {
    String User;
    String Name;

    public String getUser() {
        return User;
    }

    public void setUser(String user) {
        User = user;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
